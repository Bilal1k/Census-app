# Backup raw data , comment to save backup from runs
#backup_Cal <- data_Calgary
minor_data_Cal <- data_Calgary[,c(1:13,15:28)]

# small DAs with no data has NA values, change to 0.
minor_data_Cal[is.na(minor_data_Cal)] <- 0

# clean col names "use each countries name as the 
# col name and shorten total name.
clean_census <- function(x){
  c <- str_split(x, pattern = ": ", simplify = TRUE)[,2]
  return(c)
}


minor_data_Cal[,str_detect(names(st_drop_geometry(minor_data_Cal)),pattern = "Total -")]  <- NULL
t_ind <- which(str_detect(names(st_drop_geometry(minor_data_Cal)),pattern = "Total"))
minor_data_Cal$Total <- st_drop_geometry(minor_data_Cal)[,t_ind]
minor_data_Cal[,t_ind] <- NULL
minor_data_Cal <- rename_with(minor_data_Cal,clean_census, starts_with("V_CA16"))


# make a list of minority names
minor_nm_Cal <- names(st_drop_geometry(
  minor_data_Cal)[,14:ncol(st_drop_geometry(minor_data_Cal))])


# Calculate percentages, remove NA generated by DAs with zero population
percent <- function(x){
  perc <- round(x*100/minor_data_Cal$Population, digits = 2)
  return(perc)
}


minor_data_Cal <- minor_data_Cal %>%
  mutate_at(minor_nm_Cal, percent)

minor_data_Cal[is.na(minor_data_Cal)] <- 0


# save spatial features to H.D.D as shape file, avoid pulling data multiple 
# times, col names will change due to shape file name length and capitalization
# rules.

minor_df_Cal <- st_drop_geometry(minor_data_Cal)

Cal_CT_geom <- st_geometry(minor_data_Cal)

saveRDS(minor_df_Cal, file = "minor_df_Cal.rds")
saveRDS(minor_nm_Cal, file = "minor_nm_Cal.rds")
st_write(Cal_CT_geom,"Cal_CT_geom.shp", append  = FALSE)

